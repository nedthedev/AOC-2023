def find_hz_mirror(map):
    for index in range(len(map)-1):
        if(map[index] == map[index+1]):
            if(confirm_hz_fold(map, index)):
                return index+1
    return 0

def rotate(map):
    rotated = []
    for col in range(len(map[0])):
        column = ""
        for row in range(len(map)):
            column += map[row][col]
        rotated.append(column)
    return rotated


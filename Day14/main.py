def roll_north(grid, row, col):
    while row > 0:
        if(grid[row-1][col] == '.'):
            grid[row-1] = grid[row-1][:col] + "O" + grid[row-1][col+1:]
            grid[row] = grid[row][:col] + "." + grid[row][col+1:]
            row = row-1
        else:
            break
    return grid

def print_grid(grid):
    for row in grid:
        print(row)

def get_weight(grid):
    weight = 0
    factor = len(grid)
    for row in range(len(grid)):
        for col in range(len(grid[0])):
            if(grid[row][col] == "O"):
                weight += factor
        factor -= 1
    return weight

